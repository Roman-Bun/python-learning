# Користувач вводить два числа
a = int(input("Введіть перше число: "))
b = int(input("Введіть друге число: "))
# Програма виводить:
print(f"Сумма: {a + b}")
print(f"Різниця: {a - b}")
print(f"Добуток: {a * b}")
print(f"Ділення: {a / b}")
print(f"Ціле ділення: {a // b}")
print(f"Остачa від ділення: {a % b}")
print(f"Перше число в степені другого: {a ** b}")