# 1. Створи tuple з координатами міста
#    (назва, широта, довгота)
city = ("Київ", 50.45, 30.52)
# 2. Розпакуй tuple у три змінні
name, lat, lng = city
# 3. Виведи гарно відформатований рядок
#    "Місто: Київ, координати: 50.45, 30.52"
print(f"Місто: {name}, координати: {lat}, {lng}")
# 4. Створи tuple з днями тижня
days = ("Пн", "Вт", "Ср", "Чт", "Пт", "Сб", "Нд")
# 5. Виведи тільки робочі дні (перші 5)
print(", ".join(days[:5]))
# 6. Спробуй змінити перший елемент tuple
#    і виведи помилку через try/except:
#    try:
#        ...
#    except TypeError as e:
#        print(f"Помилка: {e}")
try:
    days[0] = "Пп"
except TypeError as e:
    print(f"Помилка: {e}")